package br.com.AcaiBrasil.teste.web;

/**
 *
 * @author natha
 */
public class ItemBean {

    private Long id;
    private String descricao;
    private int valor;

    public ItemBean(Long id, String descricao, int valor) {
        this.id = id;
        this.descricao = descricao;
        this.valor = valor;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    public int getValor() {
        return valor;
    }

    public void setValor(int valor) {
        this.valor = valor;
    }

}
